namespace JMXFileEditor.Silkroad.Mathematics
{
    public class Color3
    {
        public float Red { get; set; }
        public float Green { get; set; }
        public float Blue { get; set; }

        public Color3()
        {
        }

        public Color3(float red, float green, float blue)
        {
            this.Red = red;
            this.Green = green;
            this.Blue = blue;
        }
    }
}